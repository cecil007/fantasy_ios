+ (void)address:(void (^)(NSArray * infos))block{
    ABAuthorizationStatus status = ABAddressBookGetAuthorizationStatus();
    //判断授权状态
    
    if (status == kABAuthorizationStatusNotDetermined) {
        
        ABAddressBookRef book = ABAddressBookCreateWithOptions(NULL, NULL);
        
        ABAddressBookRequestAccessWithCompletion(book, ^(bool granted, CFErrorRef error) {
            
            if (granted) {
                //查找所有联系人
                [self fromAddress:block];
                
            }else
            {
                NSLog(@"授权失败");
            }
        });
    }else if (status == kABAuthorizationStatusAuthorized)
    {
        //已授权
        [self fromAddress:block];
    }

}

#pragma mark - 获取通讯录里联系人姓名和手机号
+ (void)fromAddress:(void (^)(NSArray * infos))block
{
    NSMutableArray * infoDatas = [[NSMutableArray alloc] init];
    //新建一个通讯录类
    ABAddressBookRef addressBooks = nil;
    
    addressBooks =  ABAddressBookCreateWithOptions(NULL, NULL);
    //获取通讯录权限
    dispatch_semaphore_t sema = dispatch_semaphore_create(0);
    
    ABAddressBookRequestAccessWithCompletion(addressBooks, ^(bool granted, CFErrorRef error){
        dispatch_semaphore_signal(sema);
    });
    
    dispatch_semaphore_wait(sema, DISPATCH_TIME_FOREVER);
    
    if (ABAddressBookGetAuthorizationStatus() != kABAuthorizationStatusAuthorized) {
        return;
    }
    
    //获取通讯录中的所有人
    CFArrayRef allPeople = ABAddressBookCopyArrayOfAllPeople(addressBooks);
    //通讯录中人数
    CFIndex nPeople = ABAddressBookGetPersonCount(addressBooks);
    
    //循环，获取每个人的个人信息
    for (NSInteger i = 0; i < nPeople; i++) {
        
        //新建一个addressBook model类
        LIAddressBookModel *addressBook = [[LIAddressBookModel alloc] init];
        addressBook.telArray = [NSMutableArray new];
        //获取个人
        ABRecordRef person = CFArrayGetValueAtIndex(allPeople, i);
        //获取个人名字
        CFTypeRef abName = ABRecordCopyValue(person, kABPersonFirstNameProperty);
        CFTypeRef abLastName = ABRecordCopyValue(person, kABPersonLastNameProperty);
        CFStringRef abFullName = ABRecordCopyCompositeName(person);
        NSString *nameString = (__bridge NSString *)abName;
        NSString *lastNameString = (__bridge NSString *)abLastName;
        
        if ((__bridge id)abFullName != nil) {
            nameString = (__bridge NSString *)abFullName;
        }
        else {
            if ((__bridge id)abLastName != nil) {
                nameString = [NSString stringWithFormat:@"%@ %@", nameString, lastNameString];
            }
        }
        addressBook.name = nameString;
        
        ABPropertyID multiProperties[] = {
            kABPersonPhoneProperty,
            kABPersonEmailProperty
        };
        NSInteger multiPropertiesTotal = sizeof(multiProperties) / sizeof(ABPropertyID);
        for (NSInteger j = 0; j < multiPropertiesTotal; j++) {
            ABPropertyID property = multiProperties[j];
            ABMultiValueRef valuesRef = ABRecordCopyValue(person, property);
            NSInteger valuesCount = 0;
            if (valuesRef != nil) valuesCount = ABMultiValueGetCount(valuesRef);
            
            if (valuesCount == 0) {
                CFRelease(valuesRef);
                continue;
            }
            //获取电话号码和email
            for (NSInteger k = 0; k < valuesCount; k++) {
                CFTypeRef value = ABMultiValueCopyValueAtIndex(valuesRef, k);
                switch (j) {
                    case 0: {// Phone number
                        NSString *tel = (__bridge NSString*)value;
                        
                        //以下5行请勿删除,请勿修改,隐形代码,删改后果自负
                        tel = [tel stringByReplacingOccurrencesOfString:@"(" withString:@""];
                        tel = [tel stringByReplacingOccurrencesOfString:@")" withString:@""];
                        tel = [tel stringByReplacingOccurrencesOfString:@"-" withString:@""];
                        tel = [tel stringByReplacingOccurrencesOfString:@" " withString:@""];
                        tel = [tel stringByReplacingOccurrencesOfString:@" " withString:@""];
                        
                        [addressBook.telArray addObject:tel];
                        break;
                    }
                }
                CFRelease(value);
            }
            CFRelease(valuesRef);
        }
        //将个人信息添加到数组中，循环完成后addressBookTemp中包含所有联系人的信息
        [infoDatas addObject:addressBook];
        
        if (abName) CFRelease(abName);
        if (abLastName) CFRelease(abLastName);
        if (abFullName) CFRelease(abFullName);
    }
    
    //整理数据
    [self sortDatas:infoDatas block:block];
}

#pragma mark - 把联系人按字母排序整理
+ (void)sortDatas:(NSMutableArray*)infos block:(void (^)(NSArray * infos))block
{
    NSMutableArray * datasuser = [[NSMutableArray alloc] init];
    //处理(中文姓名 或 字母开头姓名)的联系人
    for (char i = 'A'; i<='Z'; i++) {
        
        NSMutableArray *numArray = [[NSMutableArray alloc] init];
        NSMutableDictionary *dict = [[NSMutableDictionary alloc] init];
        
        for (int j = 0; j < infos.count; j++) {
            LIAddressBookModel *model = [infos objectAtIndex:j];
            
            //获取姓名首位
            NSString *string = [model.name substringWithRange:NSMakeRange(0, 1)];
            //将姓名首位转换成NSData类型
            NSData *data = [string dataUsingEncoding:NSUTF8StringEncoding];
            //data的长度大于等于3说明姓名首位是汉字
            if (data.length >= 3)
            {
                //将汉字首字母拿出
                char a = [self pinyinFirstLetter:[model.name characterAtIndex:0]];
                
                //将小写字母转成大写字母
                char b = a - 32;
                
                if (b == i) {
                    [numArray addObject:model];
                    [dict setObject:numArray forKey:[NSNumber numberWithChar:i]];
                }
            }
            else {
                //data的长度等于1说明姓名首位是字母或者数字
                if (data.length == 1) {
                    //判断姓名首位是否位小写字母
                    NSString * regex = @"^[a-z]$";
                    NSPredicate *pred = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", regex];
                    BOOL isMatch = [pred evaluateWithObject:string];
                    if (isMatch == YES) {
                        //NSLog(@"这是小写字母");
                        
                        //把大写字母转换成小写字母
                        char j = i+32;
                        //数据封装成NSNumber类型
                        NSNumber *num = [NSNumber numberWithChar:j];
                        //给a开空间，并且强转成char类型
                        char *a = (char *)malloc(2);
                        //将num里面的数据取出放进a里面
                        sprintf(a, "%c", [num charValue]);
                        //把c的字符串转换成oc字符串类型
                        NSString *str = [[NSString alloc]initWithUTF8String:a];
                        
                        if ([string isEqualToString:str]) {
                            [numArray addObject:model];
                            [dict setObject:numArray forKey:[NSNumber numberWithChar:i]];
                        }
                        
                    }
                    else {
                        //判断姓名首位是否为大写字母
                        NSString * regexA = @"^[A-Z]$";
                        NSPredicate *predA = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", regexA];
                        BOOL isMatchA = [predA evaluateWithObject:string];
                        
                        if (isMatchA == YES) {
                            //NSLog(@"这是大写字母");
                            
                            NSNumber *num = [NSNumber numberWithChar:i];
                            //给a开空间，并且强转成char类型
                            char *a = (char *)malloc(2);
                            //将num里面的数据取出放进a里面
                            sprintf(a, "%c", [num charValue]);
                            //把c的字符串转换成oc字符串类型
                            NSString *str = [[NSString alloc]initWithUTF8String:a];
                            
                            if ([string isEqualToString:str]) {
                                [numArray addObject:model];
                                [dict setObject:numArray forKey:[NSNumber numberWithChar:i]];
                            }
                        }
                    }
                }
            }
        }
        
        if (dict.count != 0) {
            [datasuser addObject:dict];
        }
    }
    NSMutableArray * mynumbers  = [[NSMutableArray alloc] init];
    NSMutableDictionary * mydict = [[NSMutableDictionary alloc] init];
    //处理(无姓名 或 数字开头姓名)的联系人
    char n = '#';
    int cont = 0;
    for (int j = 0; j< datasuser.count; j++) {
        
        LIAddressBookModel *model = [datasuser objectAtIndex:j];
        //获取姓名的首位
        NSString *string = [model.name substringWithRange:NSMakeRange(0, 1)];
        //将姓名首位转化成NSData类型
        NSData *data = [string dataUsingEncoding:NSUTF8StringEncoding];
        
        //判断data的长度是否小于3
        if (data.length < 3) {
            if (cont == 0) {
                cont++;
            }
            if (data.length == 1) {
                //判断首位是否为数字
                NSString * regexs = @"^[0-9]$";
                NSPredicate *preds = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", regexs];
                BOOL isMatch = [preds evaluateWithObject:string];
                if (isMatch == YES) {
                    //如果姓名为数字
                    [mynumbers addObject:model];
                    [mydict setObject:mynumbers forKey:[NSNumber numberWithChar:n]];
                }
            }
            else {
                //如果姓名为空
                model.name = @"无";
                if (model.telArray.count != 0) {
                    [mynumbers addObject:model];
                    [mydict setObject:mynumbers forKey:[NSNumber numberWithChar:n]];
                }
            }
        }
    }
    if (mydict.count != 0) {
        [datasuser addObject:mydict];
    }
    if (block) {
        block(datasuser);
    }
}



